public class ChangeChar{
	public static void main(String args[]){
		// \t 对齐 - 制表符
		System.out.println("bj\ttj\tshh");
		// \n 换行
		System.out.println("java\n");
		// \\
		System.out.println("hello\\\\mlian");
		// \' 
		// \"
		System.out.println("\"ad\"a\'s\"");
		// \r 回车 - 同行
		System.out.println("asd\r\nllhsa");
	}
}





